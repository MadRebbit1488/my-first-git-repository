//Оголошення ф-ції видно тільки в межах блоку.
let age = prompt("Скільки вам років?");
let welcome;

if (age <18){

    welcome = function () {
        console.log("Привіт!");
    };
}else {
    welcome = function () {
        console.log("Доброго дня!");  
    };
}

welcome ();

